# Criar um programa que calcule a quantidade de pesso de um pedido
peso_do_tubeti_padrão = float(input('Qual valor do peso do Tubete?:'))
peso_do_pedido = float(input('Qual valor do peso dos Kilos ou toneladas ?:'))
peso_da_bobina = float(input('Qual valor do peso da Bobina?:'))
quantas_peças = float(input('Quantas peças Amaquina Produz?:'))
peso_do_palete = float(input('Qual è o peso do palete?:'))
resultado = float (peso_do_pedido / peso_da_bobina)
Resultadofinal = resultado * quantas_peças
print('O tamanho do Tubete é {:.2f} \n A quantidade peças  é {:.6f}\n O peso do palete é {:.3f}'.format(peso_do_tubeti_padrão, Resultadofinal, peso_do_palete))
